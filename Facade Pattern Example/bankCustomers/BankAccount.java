package bankCustomers;

import framework.*;

/**
 * Simplified bank account.
 */
class BankAccount implements Account   // Note: not public
{
	private int balance = 0;    
	private int number = 999;   
	
/*****************************************************************************************************
 */
public BankAccount()
/*****************************************************************************************************/
{	super();
}

/*****************************************************************************************************
 */
public BankAccount( int anAccountNumber )
/*****************************************************************************************************/ 
{
	super();
	number = anAccountNumber;
}

/*****************************************************************************************************
 */
public int getAccountNum()
/*****************************************************************************************************/
{	return number;
}

/*****************************************************************************************************
 */
public int getBalance()
/*****************************************************************************************************/
{	return balance;
}

/*****************************************************************************************************
 * Postcondition: 'this.balance' increased by 'aDepositAmount'
 */
public void deposit( int aDepositAmount )
/*****************************************************************************************************/
{	this.balance += aDepositAmount;
}

}